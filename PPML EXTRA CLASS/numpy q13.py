import numpy as np

# Random float numbers (0 to 1)
arr1 = np.random.rand(5)
print("Random floats:", arr1)

# Random integers between 1 and 50
arr2 = np.random.randint(1, 50, 5)
print("Random integers:", arr2)

# 2D random array
arr3 = np.random.rand(2, 3)
print("2D random array:\n", arr3)