import numpy as np

# Create two arrays
arr1 = np.array([10, 20, 30])
arr2 = np.array([1, 2, 3])

# Addition
add = arr1 + arr2

# Subtraction
sub = arr1 - arr2

# Multiplication (element-wise)
mul = arr1 * arr2

print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)